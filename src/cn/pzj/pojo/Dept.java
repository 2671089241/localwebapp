package cn.pzj.pojo;

public class Dept {

	private Integer doptNo;
	
	private String name;
	
	private String loc;

	public Integer getDoptNo() {
		return doptNo;
	}

	public void setDoptNo(Integer doptNo) {
		this.doptNo = doptNo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLoc() {
		return loc;
	}

	public void setLoc(String loc) {
		this.loc = loc;
	}
	
	
}
